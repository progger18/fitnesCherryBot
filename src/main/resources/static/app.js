// –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Ä–∞—Å—á–µ—Ç–∞ —Å—É—Ç–æ—á–Ω–æ–π –Ω–æ—Ä–º—ã –∫–∞–ª–æ—Ä–∏–π
function calculateDailyCalories() {
    const weight = parseFloat(document.getElementById('weight').value);
    const height = parseFloat(document.getElementById('height').value);
    const age = parseInt(document.getElementById('age').value);
    const activityLevel = document.getElementById('activityLevel').value;
    const goal = document.getElementById('goal').value;

    if (!weight || !height || !age) {
        showResult('caloriesResult', '–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∑–∞–ø–æ–ª–Ω–∏—Ç–µ –≤—Å–µ –ø–æ–ª—è');
        return;
    }

    // –†–∞—Å—á–µ—Ç –±–∞–∑–æ–≤–æ–≥–æ –æ–±–º–µ–Ω–∞ –≤–µ—â–µ—Å—Ç–≤ (—Ñ–æ—Ä–º—É–ª–∞ –ú–∏—Ñ—Ñ–ª–∏–Ω–∞-–°–∞–Ω –ñ–µ–æ—Ä–∞)
    let bmr = 10 * weight + 6.25 * height - 5 * age + 5;

    // –ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏
    let activityMultiplier;
    switch (activityLevel) {
        case 'sedentary':
            activityMultiplier = 1.2;
            break;
        case 'light':
            activityMultiplier = 1.375;
            break;
        case 'moderate':
            activityMultiplier = 1.55;
            break;
        case 'active':
            activityMultiplier = 1.725;
            break;
        case 'very_active':
            activityMultiplier = 1.9;
            break;
    }

    // –†–∞—Å—á–µ—Ç —Å—É—Ç–æ—á–Ω–æ–π –Ω–æ—Ä–º—ã –∫–∞–ª–æ—Ä–∏–π
    let tdee = bmr * activityMultiplier;

    // –ö–æ—Ä—Ä–µ–∫—Ç–∏—Ä–æ–≤–∫–∞ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Ü–µ–ª–∏
    switch (goal) {
        case 'lose':
            tdee -= 500; // –î–µ—Ñ–∏—Ü–∏—Ç –∫–∞–ª–æ—Ä–∏–π –¥–ª—è –ø–æ—Ö—É–¥–µ–Ω–∏—è
            break;
        case 'maintain':
            // –û—Å—Ç–∞–≤–ª—è–µ–º –∫–∞–∫ –µ—Å—Ç—å
            break;
        case 'gain':
            tdee += 500; // –ü—Ä–æ—Ñ–∏—Ü–∏—Ç –∫–∞–ª–æ—Ä–∏–π –¥–ª—è –Ω–∞–±–æ—Ä–∞ –º–∞—Å—Å—ã
            break;
    }

    // –†–∞—Å—á–µ—Ç –º–∞–∫—Ä–æ–Ω—É—Ç—Ä–∏–µ–Ω—Ç–æ–≤
    const protein = Math.round(weight * 2); // 2–≥ –±–µ–ª–∫–∞ –Ω–∞ –∫–≥ –≤–µ—Å–∞
    const fat = Math.round(weight * 0.8); // 0.8–≥ –∂–∏—Ä–æ–≤ –Ω–∞ –∫–≥ –≤–µ—Å–∞
    const carbs = Math.round((tdee - (protein * 4 + fat * 9)) / 4); // –û—Å—Ç–∞–≤—à–∏–µ—Å—è –∫–∞–ª–æ—Ä–∏–∏ –∏–∑ —É–≥–ª–µ–≤–æ–¥–æ–≤

    const result = `–í–∞—à–∞ —Å—É—Ç–æ—á–Ω–∞—è –Ω–æ—Ä–º–∞ –∫–∞–ª–æ—Ä–∏–π: ${Math.round(tdee)} –∫–∫–∞–ª\n\n` +
                  `–†–µ–∫–æ–º–µ–Ω–¥—É–µ–º–æ–µ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –º–∞–∫—Ä–æ–Ω—É—Ç—Ä–∏–µ–Ω—Ç–æ–≤:\n` +
                  `‚Ä¢ –ë–µ–ª–∫–∏: ${protein}–≥ (${protein * 4} –∫–∫–∞–ª)\n` +
                  `‚Ä¢ –ñ–∏—Ä—ã: ${fat}–≥ (${fat * 9} –∫–∫–∞–ª)\n` +
                  `‚Ä¢ –£–≥–ª–µ–≤–æ–¥—ã: ${carbs}–≥ (${carbs * 4} –∫–∫–∞–ª)\n\n` +
                  `–†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏:\n` +
                  `‚Ä¢ –ü–∏—Ç–∞–π—Ç–µ—Å—å 4-6 —Ä–∞–∑ –≤ –¥–µ–Ω—å\n` +
                  `‚Ä¢ –ü–µ–π—Ç–µ 2-3 –ª–∏—Ç—Ä–∞ –≤–æ–¥—ã\n` +
                  `‚Ä¢ –°–ª–µ–¥–∏—Ç–µ –∑–∞ –∫–∞—á–µ—Å—Ç–≤–æ–º –ø—Ä–æ–¥—É–∫—Ç–æ–≤\n` +
                  `‚Ä¢ –í–µ–¥–∏—Ç–µ –¥–Ω–µ–≤–Ω–∏–∫ –ø–∏—Ç–∞–Ω–∏—è`;

    showResult('caloriesResult', result);
}

// –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
function showResult(elementId, text) {
    const element = document.getElementById(elementId);
    element.innerHTML = text.replace(/\n/g, '<br>');
    element.style.display = 'block';
} 